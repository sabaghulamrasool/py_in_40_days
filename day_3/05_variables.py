# rules to assign a variable

# 1. variable name should be a string of characters (a-z,A-Z,0-)
# 2. variable name should not be a keyword in python
# 3. variable should not start with number
# 4. variable name should not contain any special character except underscore
# 5. variable name should be unique
# 6. variable name should not be too long
# 7. variable name should be meaningful
# 8. variable name should be in camel case or underscore notation
# 9. variable name should not be a reserved word in python
# 10. variable name should not be a function name in python
# 11. variable name should not be a class name in python
# 12. do not use spaces


